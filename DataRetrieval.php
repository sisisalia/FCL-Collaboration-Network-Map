// Comparing the value in the projectDuration
function projectDuration($date){
    if($date == '< 6 months') return 1;
    if($date == '6 - 12 months') return 2;
    if($date == '12 months - 2 years') return 3;
    if($date == '> 2 years') return 4;
}

// Turning a string into an array
function turnToArray($original){
        $replaced = str_replace( "[","", $original);
        $replaced = str_replace( "]", "", $replaced);
        $array = split(',',$replaced);
        for($j = 0; $j < sizeof($array) ; $j++){
            if($array[$j] == '"Other"'){
                unset($array[$j]);
                $array = array_values($array);
            }
        }
        $array = str_replace('"','',$array);
        return ($array);
}

// To check if a string is inside an array
function inside($new, $oldarray){
    for($n = 0; $n < sizeof($oldarray); $n++)
    {
        if($oldarray[$n] == $new)
        {
            return 1;
        }
    }
    return 0;
}
